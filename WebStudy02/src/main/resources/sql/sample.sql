
--P101000001 상품의 기본 정보와 해당 상품의 제조사명, 제조사 담당자명을 조회하세요
SELECT P.*, B.BUYER_NAME, B.BUYER_CHARGER
FROM PROD P INNER JOIN BUYER B ON (P.BUYER_ID = B.BUYER_ID)
WHERE P.PROD_ID = 'P101000001';

SELECT PROD_NAME, PROD_ID, BUYER_ID, BUYER_NAME, BUYER_CHARGER
FROM PROD NATURAL JOIN BUYER
WHERE PROD_ID = 'P101000001';

-- P10101 제조사의 기본 정보와 해당 제조사와의 거래 품목(품목코드, 품목명) 조회
SELECT B.*, PROD_ID, PROD_NAME
FROM BUYER B RIGHT OUTER JOIN PROD P ON (P.BUYER_ID = B.BUYER_ID)
WHERE B.BUYER_ID = 'P10101';

-- 거래 품목이 없는 제조사만 조회. 제조사 조회, 거래품목이 없는 제조사를 조건으로
SELECT *
FROM BUYER
WHERE BUYER_ID NOT IN (SELECT BUYER_ID FROM PROD);

-- 회원들 중에서 상품을 한번이상 구매(CART)한 적이 있는 회원의 기본 정보 조회(MEMBER)
