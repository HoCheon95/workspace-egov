CREATE OR REPLACE VIEW PRODVIEW AS 

SELECT
PROD_ID,
PROD_NAME,
P.LPROD_GU,
P.BUYER_ID,
PROD_COST,
PROD_PRICE,
PROD_SALE,
PROD_OUTLINE,
PROD_DETAIL,
PROD_IMG,
PROD_TOTALSTOCK,
PROD_INSDATE,
PROD_PROPERSTOCK,
PROD_SIZE,
PROD_COLOR,
PROD_DELIVERY,
PROD_UNIT,
PROD_QTYIN,
PROD_QTYSALE,
PROD_MILEAGE,
LPROD_NAME,
BUYER_NAME,
BUYER_BANK,
BUYER_BANKNO,
BUYER_BANKNAME,
BUYER_ZIP,
BUYER_ADD1,
BUYER_ADD2,
BUYER_COMTEL,
BUYER_FAX,
BUYER_MAIL,
BUYER_CHARGER,
