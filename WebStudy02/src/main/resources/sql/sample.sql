FROM MEMBER NATURAL JOIN CART;

-- 회원의 기본 정보(MEMBER)와 구매 상품 코드(CART) 조회
SELECT *
FROM MEMBER M LEFT OUTER JOIN CART C ON (M.MEM_ID = C.MEM_ID)
WHERE M.MEM_ID = 'n001';

--P101000001 상품 코드, 상품명(PROD), 분류 코드, 분류명(LPROD)와 해당 상품의 제조사명, 제조사 담당자명(BUYER)을 조회하세요
SELECT P.PROD_ID, P.PROD_NAME, P.LPROD_GU, LPROD_NAME, BUYER_NAME, B.BUYER_CHARGER
FROM PROD P INNER JOIN LPROD L ON (P.LPROD_GU = L.LPROD_GU)
            INNER JOIN BUYER B ON (P.BUYER_ID = B.BUYER_ID)
WHERE PROD_ID = 'P101000001';

-- P10101 제조사의 기본 정보와 해당 제조사와의 거래 품목(품목코드, 품목명, 상품분류명) 조회
SELECT B.*, PROD_ID, PROD_NAME, P.LPROD_GU
, LPROD_NAME
FROM BUYER B LEFT OUTER JOIN PROD P ON (P.BUYER_ID = B.BUYER_ID)
             LEFT OUTER JOIN LPROD L ON (P.LPROD_GU = L.LPROD_GU)
WHERE B.BUYER_ID = 'P10103';

